# N times "Hello World":
# You are given an integer n. Print "Hello World" n times.
# Sample Input: 
# 5
# Sample Output: 
# Hello World
# Hello World
# Hello World
# Hello World
# Hello World
n = int(input())
counter = 0
while counter <n:
  print("Hello"+ " "+" World ")
  counter+=1
exit()
